package com.post.station.response;

public class PenaltyBean {
    public String date;
    public String benefitsplace;
    public String benefits;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getBenefitsplace() {
        return benefitsplace;
    }

    public void setBenefitsplace(String benefitsplace) {
        this.benefitsplace = benefitsplace;
    }

    public String getBenefits() {
        return benefits;
    }

    public void setBenefits(String benefits) {
        this.benefits = benefits;
    }
}